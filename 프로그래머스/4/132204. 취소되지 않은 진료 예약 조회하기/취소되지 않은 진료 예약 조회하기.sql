SELECT A.APNT_NO
    , B.PT_NAME
    , A.PT_NO
    , A.MCDP_CD
    , C.DR_NAME
    , A.APNT_YMD
FROM APPOINTMENT A
JOIN PATIENT B USING (PT_NO)
JOIN DOCTOR C ON A.MDDR_ID = C.DR_ID
# WHERE DATE_FORMAT(APNT_YMD, '%Y-%m-%d') = '2022-04-13'
WHERE APNT_YMD LIKE '2022-04-13%'  
AND A.APNT_CNCL_YN = 'N'
AND A.MCDP_CD = 'CS'
ORDER BY A.APNT_YMD
;